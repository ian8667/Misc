# Stops the OEM agent. (Example program)
#
# Dependency:
# ------------------------------------------------------------------

header()
{
  echo " "
  date "+DATE: %A %F%nTIME: %H:%M:%S"
  echo " "
  echo $*
  echo " "
}

# ------------------------------------------------------------------

trailer()
{
  echo " "
  date "+TIME: %H:%M:%S"
  echo $*
  echo " "
}

# ------------------------------------------------------------------

body()
{
  AGENT_HOME=/u01/app/oracle/product/agent/core/12.1.0.4.0

  header "Stopping OEM agent"
  $AGENT_HOME/bin/emctl stop agent
  trailer "Done"
}

# ------------------------------------------------------------------
# Main routine start here
# ------------------------------------------------------------------
body

